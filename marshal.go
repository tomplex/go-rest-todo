package main

import (
	"database/sql"
	"encoding/json"
	"github.com/lib/pq"
	"time"
)

// helpful types and methods taken from https://stackoverflow.com/questions/33072172/how-can-i-work-with-sql-null-values-and-json-in-golang-in-a-good-way
type JsonNullInt64 struct {
	sql.NullInt64
}

type JsonNullString struct {
	sql.NullString
}

type JsonNullBool struct {
	sql.NullBool
}

type JsonNullTime struct {
	pq.NullTime
}

func (v JsonNullInt64) MarshalJSON() ([]byte, error) {
	if v.Valid {
		return json.Marshal(v.Int64)
	} else {
		return json.Marshal(nil)
	}
}

func (v *JsonNullInt64) UnmarshalJSON(data []byte) error {
	// Unmarshalling into a pointer will let us detect null
	var x *int64
	if err := json.Unmarshal(data, &x); err != nil {
		return err
	}
	if x != nil {
		v.Valid = true
		v.Int64 = *x
	} else {
		v.Valid = false
	}
	return nil
}

